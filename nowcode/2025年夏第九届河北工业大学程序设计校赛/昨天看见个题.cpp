int qpow(int a, int b, int p) {
    int ans = 1;
    while (b) {
        if (b & 1) ans = (ill)a * ans % p;
        a = (ill)a * a % p;
        b >>= 1;
    }
    return ans;
}
// p为质数
int invp(int a, int p) { return qpow(a, p - 2, p); }

void work() {
    int n;
    int k, m;
    cin >> n >> k >> m;
    int u = 1, v = 1;
    int km = k * m;
    for (int i = 0; i < n; ++i) {
        int x, y;
        cin >> x >> y;
        u = (ill)u * qpow(x, y, k) % k;
        v = (ill)v * qpow(x, y, km) % km;
    }
    int tt = (v - u) % km;
    if (tt < 0) tt += km;
    int res = (tt / k) % m;
    cout << res << '\n';
}

